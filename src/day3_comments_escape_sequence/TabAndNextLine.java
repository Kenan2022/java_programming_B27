package day3_comments_escape_sequence;

import static java.lang.System.out;

public class TabAndNextLine {
public static void main(String[] args) {

    System.out.println("Thi is not tabb");
    System.out.println("\tThi is not tabb");
    System.out.println("\t\tThi is not tabb");
    System.out.println("     Thi is not tabb");

    System.out.println("\t\t\t\texample");
    System.out.println("example");


    System.out.println();
    System.out.println("1\"example");
    System.out.println("2)example");




}










}






